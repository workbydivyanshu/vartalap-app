export default function EmojiPicker({ onSelect, onClose }: EmojiPickerProps) {
  const [activeCategory, setActiveCategory] = useState(0);
  const [search, setSearch] = useState('');

  const filteredEmojis = search
    ? categories.flatMap((c) => c.emojis).filter((e) => e.includes(search))
    : categories[activeCategory].emojis;

  return (
    <div className="emoji-picker-container" onClick={(e) => e.stopPropagation()}>
      <div className="emoji-picker-header">
        <input
          className="emoji-picker-search"
          placeholder="Search emojis..."
          value={search}
          onChange={(e) => setSearch(e.target.value)}
        />
      </div>
      <div className="emoji-picker-categories">
        {categories.map((cat, i) => (
          <button
            key={i}
            className={`emoji-category-btn ${activeCategory === i && !search ? 'active' : ''}`}
            onClick={() => { setActiveCategory(i); setSearch(''); }}
            title={cat.name}
          >
            {cat.icon}
          </button>
        ))}
      </div>
      <div className="emoji-picker-grid">
        {filteredEmojis.map((emoji, i) => (
          <button
            key={i}
            className="emoji-item"
            onClick={() => { onSelect(emoji); onClose(); }}
          >
            {emoji}
          </button>
        ))}
        {filteredEmojis.length === 0 && (
          <div className="emoji-picker-empty">No emojis found</div>
        )}
      </div>
    </div>
  );
}
